package com.paypocket.model;

/**
 * Роль пользователя — основа ролевой модели безопасности.
 *
 * <ul>
 *   <li><b>USER</b>  — обычный пользователь. Работает только со своими
 *       кошельками и транзакциями (функционал не изменился).</li>
 *   <li><b>ADMIN</b> — администратор. Дополнительно может просматривать
 *       всех пользователей, их кошельки и все транзакции системы
 *       через эндпоинты {@code /api/v1/admin/**}.</li>
 * </ul>
 *
 * <p>Роль назначается на уровне БД (сид-админ создаётся Flyway-миграцией),
 * а не через регистрацию — самоназначение администратора невозможно.</p>
 */
public enum Role {
    USER,
    ADMIN
}
